				<h2>Who I am.</h2>
				<p>I am currently a Senior Creative Technologist at <a href="http://www.mullen.com" target="_blank">Mu<em>ll</em>en</a> in downtown Boston.</p>
				
				<p>I work constantly to ensure our team is well equiped to meet any problems head-on with confidence. Building creative, interactive experiences should always be a group effort, not a competition.</p>
				
				<p>As a Creative Technologist I constantly strive to ensure our sites keep their perfectly designed integrity while also looking just as perfect across all browsers and devices. I work directly with the Design department to ensure we both deliver exactly what our clients are looking for. In any site's I build, the user is the most important part of the experience.</p>
				
				<p>When it comes to fixing problems, there's no such a thing as a blackhole bug to me. If IE6 doesn't show padding right, it's probably due to it's Box Model parsing problems, or if IE7 isn't rendering an image, you might not have saved its color profile correctly. The key to solving any problem (Internet-related or not) is learning from all your past mistakes and thinking fast on your feet.</p>
				
				<p>I love using <a href="http://jquery.com" target="_blank">jQuery</a>, <a href="http//csstricks.com" target="_blank">CSS</a> and <a href="http://php.net" target="_blank">PHP</a> when I build websites.</p>
				
